package Computer;

public class ComputerUsingLongPrimitives {

    long add(long x, long y){
        return x+y;
    }
    long subtract(long x, long y){
        return x-y;
    }
    long multiple(long x, long y){
        return x*y;
    }
    long divide(long x, long y){
        return x/y;
    }
}
